public class MultiLinePrint 
{
    public static void main(String[] args) 
    {
        // Print first line and then move cursor to the next line
        System.out.println("Java is powerful!"); 

        // Print second line and then move cursor to the next line
        System.out.println("Java is platform-independent!"); 

        // Print third line and then move cursor to the next line
        System.out.println("Java is Object-Oriented!"); 
    }
}
